import streamlit as st

# إعدادات الصفحة الأساسية
st.set_page_config(page_title="مكتبة أيوب الذكية", page_icon="🎨", layout="centered")

# عرض اللوجو والاسم
st.title("مكتبة أيوب الذكية 🧠🎨")
st.subheader("عالم التصميم، الذكاء الاصطناعي، والخدمات التقنية")

# --- قسم المساعد الذكي ---
st.write("---")
سؤال = st.text_input("بشنو أگدر أساعدك؟ (اطلب خدمات التصميم أو اسأل عن المكتبة)")

if st.button("اسأل أيوب الذكي"):
    # 1. التوقيتات
    if any(word in سؤال for word in ["وكت", "تفتحون", "تسدون", "ساعة"]):
        st.success("نفتح عيوني من الـ 8 الصبح للـ 10 بالليل، ونستقبل طلبات التصميم أونلاين 24 ساعة!")

    # 2. خدمات الذكاء الاصطناعي والتصميم (التركيز الجديد)
    elif any(word in سؤال for word in ["ذكاء", "شعارات", "لوغو", "تصميم", "صورة", "AI"]):
        st.success("إحنا خبراء بصناعة الصور واللوغوات الاحترافية بالذكاء الاصطناعي. نصمم لك هوية بصرية كاملة لعملك بلمسة عالمية.")

    # 3. المواقع والتطبيقات
    elif any(word in سؤال for word in ["موقع", "تطبيق", "برمجة", "ويب"]):
        st.info("نعم عيوني، نصمم ونبرمج مواقع إلكترونية وتطبيقات موبايل احترافية تناسب شغلك وتطوره.")

    # 4. السي في (CV) والخدمات المكتبية
    elif any(word in سؤال for word in ["سي في", "CV", "سيرة", "استنساخ", "قرطاسية"]):
        st.success("نسوي لك CV احترافي يخليك تتوظف بأحسن مكان، بالإضافة لكل خدمات القرطاسية والسحب الملون.")

    # 5. التوصيل والموقع
    elif any(word in سؤال for word in ["توصيل", "وين", "موقعك"]):
        st.warning("موقعنا: بغداد - اليوسفية - حي الصقور. وعدنا توصيل لكل المحافظات.")

    # 6. الماستر كارد
    elif any(word in سؤال for word in ["ماستر", "سحب", "كي كارد", "فلوس"]):
        st.success("متوفر سحب نقدي مباشر لكل البطاقات الإلكترونية وبكل سهولة.")

    # 7. الأسعار (الهدايا والخدمات)
    elif "بيش" in سؤال or "سعر" in سؤال:
        st.info("الهدايا تبدأ من 7 آلاف، أما خدمات التصميم والبرمجة فأسعارها تنافسية جداً وتعتمد على طلبك. تواصل ويانا وما نقصر.")

    else:
        st.write("تأمر أمر عيوني، بس وضح لي طلبك (تصميم، برمجة، أو قرطاسية) وإحنا نجهز لك كلشي.")

# --- قسم طلب خدمة (تصميم أو بضاعة) ---
st.write("---")
st.subheader("📝 اطلب خدمة أو منتج")
with st.form("order_form"):
    name = st.text_input("اسمك الكريم")
    phone = st.text_input("رقم الموبايل")
    service_type = st.selectbox("نوع الطلب", ["تصميم لوغو/شعار", "بناء موقع/تطبيق", "عمل سي في (CV)", "طلب قرطاسية/هدايا", "سحب ذكاء اصطناعي"])
    details = st.text_area("تفاصيل طلبك (شنو اللي ببالك؟)")
    submit = st.form_submit_button("إرسال الطلب")
    
    if submit:
        if name and phone and details:
            st.balloons()
            st.success(f"عاشت إيدك يا {name}، وصل طلبك للقسم المختص (قسم أيوب الذكي) وراح نتواصل وياك.")
        else:
            st.error("ياريت تملي المعلومات حتى نكدر نخدمك بصورة صحيحة.")
